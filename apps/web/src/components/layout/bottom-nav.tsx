'use client'
import Link from 'next/link'
import { usePathname } from 'next/navigation'
import { useCartStore, cartCount } from '@/lib/cart-store'
import { cn } from '@/lib/cn'

const TABS = [
  { href: '/album', label: 'Album', icon: BookIcon },
  { href: '/tienda', label: 'Store', icon: ShopIcon },
  { href: '/stats', label: 'Stats', icon: ChartIcon },
  { href: '/swap', label: 'Swap', icon: QrIcon },
]

export function BottomNav() {
  const pathname = usePathname()
  const cart = useCartStore((s) => s.cart)
  const count = cartCount(cart)

  return (
    <nav className="fixed bottom-0 left-0 right-0 bottom-tab z-50 bg-white border-t border-gray-200 flex" style={{ height: 'calc(64px + env(safe-area-inset-bottom, 0px))' }}>
      {TABS.map(({ href, label, icon: Icon }) => {
        const active = pathname.startsWith(href)
        return (
          <Link key={href} href={href} className={cn('flex-1 flex flex-col items-center justify-center gap-0.5 pt-2 pb-1 text-xs font-medium transition-colors', active ? 'text-verde' : 'text-gray-400 hover:text-gray-600')}>
            <Icon className="w-6 h-6" />
            <span>{label}</span>
          </Link>
        )
      })}
      {/* Cart link */}
      <Link href="/carrito" className={cn('flex-1 flex flex-col items-center justify-center gap-0.5 pt-2 pb-1 text-xs font-medium transition-colors relative', pathname.startsWith('/carrito') ? 'text-verde' : 'text-gray-400 hover:text-gray-600')}>
        <span className="relative">
          <CartIcon className="w-6 h-6" />
          {count > 0 && (
            <span className="absolute -top-1 -right-1 bg-rojo text-white text-[10px] font-bold rounded-full w-4 h-4 flex items-center justify-center leading-none">{count > 99 ? '99+' : count}</span>
          )}
        </span>
        <span>Cart</span>
      </Link>
    </nav>
  )
}

